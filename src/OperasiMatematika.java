public class OperasiMatematika {
    public static void main(String[] args) {

        // Operasi Matematika
        // Operator     Keterangan
        // +            Penjumlahan
        // -            Pengurangan
        // *            Perkalian
        // /            Pembagian
        // %            Sisa Pembagian

        int a = 1024;
        int b = 1024;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

    }
}
