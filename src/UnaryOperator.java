public class UnaryOperator {
    public static void main(String[] args) {

        // Unary Operator
        // Operator    Keterangan
        // ++          a = a + 1
        // --          a = a - 1
        // -           Negative
        // +           Positive
        // !           Boolean kebalikan

        int d = 100;
        // int e = 10;

        d++; // bisa menggunakan ++d/d++ Nilai pd angka 100 bertambah menjadi 101
        System.out.println(d);

        d--; // Nilai pd angka 100 berkurang menjadi 100
        System.out.println(d);

        // Tanda ! akan menjadi nilai kebalikan
        System.out.println(!false);
        System.out.println(!true);


    }
}
