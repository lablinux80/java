public class Method {
    public static void main(String[] args) {

        // Method

        // Method adalah block kode program yg akan berjalan saat kita panggil
        // Sebelumnya kita sudah menggunakan method println() untuk menampilkan tulisan di console
        // Untuk membuat method di Java, kita bs menggunakan kata kunci void, lalu diikuti dgn nama method, kurung() dan -
        // diakhiri dgn block.
        // Kita bs menanggil method dgn menggunakan nama method lalu diikuti dgn kurung ()
        // Di bhs pemrograman lain, method jg disebut dgn Funtion.

        sayHelloWorld();
    }
    static void sayHelloWorld() {
        System.out.println("Hello World");
    }
}
