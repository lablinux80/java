public class TipeDataNumber {

    public static void main(String[] args) {

        //Tipe Data Number
        //Integer number adalah bilang bulat
        //floating point number adalah bilang pecahan

        // Tipe Data            Min                            Max                          Size            Default
        // byte                 -128                           127                          1 byte          0
        // short                -32.678                        32.767                       2 byte          0
        // int                  -2.147.483.648                 2.147.483.647                4 byte          0
        // long                 -9.223.372.036.854.775.808     9.223.372.036.854.775.807    8 byte          0

        // Kode Integer Number
        // ===================
        byte iniByte = 127;
        short iniShort = 32_767;
        int iniInt = 10_000_000;
        long iniLong = 2_147_483_647;
        long iniLong2 = 9_223_372_036_854_775_807L;

        //Floating Point Number
        //Tipe Data             Min             Max             Size            Default
        //float                 3.4e-038        3.4+038         4 byte          0.0
        //double                1.7e-308        1.7e+308        8 byte          0.0

        float iniFloat = 10.10f;
        double iniDouble = 10.10;

        //Kode Literals Number

        int IntDecimal = 25;
        int hexaDecimal = 0xA132B;
        int binaryDecimal = 0b10101010;

        //Kode Underscore berfungsi menandakan nilai baca number dan bebas di letakan
        long amount = 1_000_000_000L;
        int sum =60_000_000;




    }
}
