public class AugmentedAssignments {
    public static void main(String[] args) {

        /*
         Augmented Assignments
         Operasi Matematika       Augmented Assignments
         a = a + 10               a += 10
         a = a - 10               a -= 10
         a = a * 10               a *= 10
         a = a / 10               a /= 10
         a = a % 10               a %= 10
        */

        int c = 100;

        c += 10;
        System.out.println(c);

        c -= 10;
        System.out.println(c);

        c *= 10;
        System.out.println(c);

        c /= 10;
        System.out.println(c);

        c %= 10;
        System.out.println(c);

    }
}
