public class TipeDataString {
    public static void main(String[] args) {

        //Tipe data String adalah tipe data yang berisikan data kumpulan karakter atau sederhananya adalah teks
        //Di Java, tipe data String direpresntasikan dengan kata kunci String
        //Untuk membuat String di Java, menggunakan karakter " (petik dua) sebelum dan setelah teksnya
        //Default value untuk String adalah null

        String firstName = "Alaric";
        String lastName = "Maximiliano";
        String fullName = firstName + " " + lastName;

        System.out.println(firstName);
        System.out.println(lastName);
        System.out.println(fullName);


    }
}
